package model.entities;

public class Vehicle {

	private String model;

	// Constructor
	public Vehicle() {

	}

	public Vehicle(String model) {
		this.model = model;
	}

	// Getters and Setters
	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

}
